function showWithTitleMessage() {
    swal("Here's a message!", "How Are You?");
}

function showSuccessMessage() {
    swal({
        title: "Are you sure?",
        type: "warning",
        showCancelButton: true,
        confirmButtonColor: "#DD6B55",
        confirmButtonText: "Yes, add it!",
        closeOnConfirm: false
    }, function () {
        swal("Added!", "", "success");
    });
}

